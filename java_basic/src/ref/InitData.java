package ref;

public class InitData {
    int value1; //초기화 안함
    int value2 = 10; //10으로 초기화
}

/*
변수와 초기화
변수의 종류
멤버변수(필드) - 클래스에 선언
지역변수 - 메서드에 선연,매개변수도 지역 변수의 한 종류이다.

변수의 값 초기화
멤버변수 - 자동초기화
인스턴스의 멤버 변수는 인스턴스 생성시 자동 초기화
int = 0, boolean = false, 참조형 = null(참조대상이 없다는 뜻)

지역변수 - 수동초기화
지역변수는 항상 직접 초기화해주어야 한다.
 */